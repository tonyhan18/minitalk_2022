#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	char	*str;

	if (fd < 0)
		return ;
	if (n == -2147483648)
	{
		write(fd, "-2147483648", 11);
		return ;
	}
	str = ft_itoa(n);
	ft_putstr_fd(str, fd);
	free(str);
	return ;
}
